from datetime import datetime
from typing import Dict, Any, Optional

def export_strategy_to_json(strategy_data: Dict[str, Any], company_name: str) -> str:
    """Export strategy to JSON format"""
    export_data = {
        "company": company_name,
        "exported_at": datetime.now().isoformat(),
        "strategy": strategy_data
    }
    return json.dumps(export_data, indent=2)

def export_strategy_to_csv(strategy_data: Dict[str, Any], company_name: str) -> str:
    """Export strategy summary to CSV format"""
    lines = [
        "Key,Value",
        f"Company,{company_name}",
        f"Exported At,{datetime.now().isoformat()}",
    ]

    for key, value in strategy_data.items():
        if isinstance(value, list):
            for i, item in enumerate(value[:5]):
                lines.append(f"{key}_{i},{item}")
        elif isinstance(value, dict):
            for k, v in value.items():
                lines.append(f"{key}_{k},{v}")
        else:
            lines.append(f"{key},{value}")

    return "\n".join(lines)

def create_strategy_summary_text(
    company: str,
    description: str,
    strategy: Dict[str, Any]
) -> str:
    """Create a text summary of the strategy"""

    summary = f"""
STRATEGIC ANALYSIS REPORT
{'=' * 60}

COMPANY: {company}
DESCRIPTION: {description}
GENERATED: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

{'=' * 60}

MARKET ANALYSIS
{'-' * 60}

"""

    # Market metrics
    if 'tam' in strategy:
        summary += f"Total Addressable Market (TAM): ${strategy['tam']:,.0f}M\n"

    if 'growth_rate' in strategy:
        summary += f"Annual Growth Rate: {strategy['growth_rate']}\n"

    if 'market_segments' in strategy:
        summary += f"\nMarket Segments:\n"
        for segment in strategy.get('market_segments', [])[:5]:
            summary += f"  - {segment}\n"

    # Opportunities
    if 'opportunities' in strategy:
        summary += f"\nMarket Opportunities:\n"
        for opp in strategy.get('opportunities', [])[:5]:
            summary += f"  - {opp}\n"

    # Barriers
    if 'entry_barriers' in strategy:
        summary += f"\nEntry Barriers:\n"
        barriers = strategy.get('entry_barriers', [])
        if isinstance(barriers, list):
            for barrier in barriers[:5]:
                summary += f"  - {barrier}\n"
        else:
            summary += f"  - {barriers}\n"

    # Funding needs
    if 'investment_needed' in strategy:
        summary += f"\nEstimated Investment Needed: ${strategy['investment_needed']:,.0f}M\n"

    # Timeline
    if 'timeline_to_profitability' in strategy:
        summary += f"Timeline to Profitability: {strategy['timeline_to_profitability']}\n"

    summary += f"\n{'=' * 60}\n"
    summary += f"Report generated by AI Business Tools\n"
    summary += f"www.aibusinesstools.com\n"

    return summary

