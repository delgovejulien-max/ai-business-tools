def create_shareable_link_data(
    company: str,
    strategy: Dict[str, Any]
) -> Dict[str, Any]:
    """Create data structure for shareable links"""

    return {
        "company": company,
        "created_at": datetime.now().isoformat(),
        "strategy_snapshot": {
            "tam": strategy.get('tam'),
            "growth_rate": strategy.get('growth_rate'),
            "opportunities_count": len(strategy.get('opportunities', [])),
            "market_segments_count": len(strategy.get('market_segments', []))
        },
        "share_id": hash(f"{company}_{datetime.now().isoformat()}") % 1000000
    }
